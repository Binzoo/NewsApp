function Main({ news }) {
  return (
    <>
      <div className=" ml-4 mt-11 grid grid-cols-3 gap-4 place-content-end ">
        {news.map((data, index) =>
          data.urlToImage === null ? null : (
            <NewSections
              key={index}
              title={data.title}
              image={data.urlToImage}
              info={data.content}
              learnMore={data.url}
            />
          )
        )}
      </div>
    </>
  );
}

